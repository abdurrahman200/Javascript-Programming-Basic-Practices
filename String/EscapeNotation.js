
// string using double code inside single code 
var str = "This is a 'string' "
console.log(str)
// Escape(character) Notation = \' string Escape '\, \n string
var str2 = 'This is a \'string\' '
console.log(str2)
// Escape(character) Notation = \n string = \n new line
var str3 = 'This is a \nstring '
console.log(str3)

// Escape(character) Notation = \t string = tab
var str3 = 'This is a \tstring '
console.log(str3)

// Escape(character) Notation = \\ string = backslash output
var str4 = 'This is a \\string '
console.log(str4)